export function searchFoods(query) {
  if (!query || query.length < 1) return [];
  const q = query.trim().toLowerCase();
  return FOOD_DATABASE.filter(food => food.name.includes(q) || food.category.includes(q));
}

// חישוב ערכים תזונתיים לפי כמות
export function calculateNutrition(food, amount) {
  const measurement = getFoodMeasurementConfig(food);
  const safeAmount = Number(amount) || measurement.defaultAmount;
  const multiplier = safeAmount / measurement.baseAmount;

  return {
    calories: Math.round(food.calories * multiplier),
    protein: Math.round(food.protein * multiplier * 10) / 10,
    carbs: Math.round(food.carbs * multiplier * 10) / 10,
    fat: Math.round(food.fat * multiplier * 10) / 10,
  };
}

export function formatFoodPortion(food, amount) {
  return getFoodMeasurementConfig(food).portionFormatter(amount);
}

// קטגוריות ייחודיות
export function getFoodCategories() {
  const cats = new Set(FOOD_DATABASE.map(f => f.category));
  return [...cats];
}
