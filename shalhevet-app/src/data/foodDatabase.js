const DEFAULT_WEIGHT_PORTIONS = {
  'bread-whole': 35,
  'bread-white': 30,
  dates: 24,
  'olive-oil': 14,
  'peanut-butter': 16,
  tahini: 15,
  'coconut-oil': 14,
};

function formatAmount(value) {
  const rounded = Math.round(Number(value || 0) * 10) / 10;
  return Number.isInteger(rounded) ? String(rounded) : rounded.toFixed(1);
}

function parseEmbeddedPortion(unit = '') {
  const directMatch = String(unit).match(/^\s*(\d+(?:\.\d+)?)\s*(גרם|מ"ל)/i);
  if (directMatch) {
    return Number(directMatch[1]);
  }

  const parenthesisMatch = String(unit).match(/\((\d+(?:\.\d+)?)\s*(גרם|מ"ל)\)/i);
  if (parenthesisMatch) {
    return Number(parenthesisMatch[1]);
  }

  return null;
}

export function getFoodMeasurementConfig(food) {
  const countBased = COUNT_BASED_FOODS[food.id];
  if (countBased) {
    return {
      mode: 'count',
      baseAmount: 1,
      defaultAmount: 1,
      inputLabel: 'כמות ביחידות',
      suffixLabel: countBased.label,
      referenceText: `${food.calories} קל׳ ל-${food.unit}`,
      portionFormatter: amount => `${formatAmount(amount)} ${countBased.label}`,
    };
  }

  const isVolume = /מ"ל/i.test(food.unit);
  const defaultAmount = DEFAULT_WEIGHT_PORTIONS[food.id] ?? parseEmbeddedPortion(food.unit) ?? 100;
  const referenceUnit = isVolume ? '100 מ"ל' : '100 גרם';
  const hasSuggestedPortion = defaultAmount !== 100;

  return {
    mode: isVolume ? 'volume' : 'weight',
    baseAmount: 100,
    defaultAmount,
    inputLabel: isVolume ? 'כמות במ"ל' : 'כמות בגרמים',
    suffixLabel: isVolume ? 'מ"ל' : 'גרם',
    referenceText: `${food.calories} קל׳ ל-${referenceUnit}${
      hasSuggestedPortion ? ` · מנה נפוצה: ${food.unit}` : ''
    }`,
    portionFormatter: amount => `${formatAmount(amount)} ${isVolume ? 'מ"ל' : 'גרם'}`,
  };
}

export function getFoodReferenceText(food) {
  return getFoodMeasurementConfig(food).referenceText;
}

